    private void editButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_editButtonMouseClicked

        currentDiscipline = null;
        emptyControls();

        TeacherService teacherService = new TeacherService();
        List<Teacher> teachers = teacherService.getAll();

        DefaultComboBoxModel defaultComboBoxModelTeachers = new DefaultComboBoxModel();

        defaultComboBoxModelTeachers.addAll(teachers);

        teacherComboBox.setModel(defaultComboBoxModelTeachers);

        CourseService courseService = new CourseService();
        List<Course> courses = courseService.getAll();

        DefaultComboBoxModel defaultComboBoxModelCourses = new DefaultComboBoxModel();

        defaultComboBoxModelCourses.addAll(courses);

        courseComboBox.setModel(defaultComboBoxModelCourses);

        int column = 0;
        int row = entityTable.getSelectedRow();

        if (row < 0){
            setLblColor(editButton);
            resetLblColor(listButton);
            editPanel.setVisible(true);
            listPanel.setVisible(false);
            return;
        }

        String disciplineName = entityTable.getModel().getValueAt(row, column).toString();
        DisciplineService specialityService = new DisciplineService();
        currentDiscipline = specialityService.getDiscipline(disciplineName);
        if( currentDiscipline == null )
            return;

        fillFields();
        setLblColor(editButton);
        resetLblColor(listButton);
        editPanel.setVisible(true);
        listPanel.setVisible(false);
        return;

    }//GEN-LAST:event_editButtonMouseClicked

    private void fillFields() {
        nameField.setText(currentDiscipline.getName());
        teacherComboBox.setSelectedItem(currentDiscipline.getTeacher());
        courseComboBox.setSelectedItem(currentDiscipline.getCourse());
    }

    private void emptyControls() {

        nameField.setText("");
        teacherComboBox.setSelectedItem(0);
        courseComboBox.setSelectedItem(0);
    }

    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        if( !validateData() )
            return;

        if( currentDiscipline == null )
            currentDiscipline = new Discipline();

        currentDiscipline.setName(nameField.getText());
        currentDiscipline.setTeacher((Teacher) teacherComboBox.getSelectedItem());
        currentDiscipline.setCourse((Course) courseComboBox.getSelectedItem());

        DisciplineService disciplineService = new DisciplineService();
        disciplineService.saveOrUpdate(currentDiscipline);

        setLblColor(listButton);
        resetLblColor(editButton);

        editPanel.setVisible(false);
        listPanel.setVisible(true);
        initEntityTable();
    }//GEN-LAST:event_saveButtonActionPerformed

    private boolean validateData() {

        errorLabel.setText("");

        if( !FieldValidator.validateAlphabetical(nameField,errorLabel) )
        {
            errorLabel.setText("Set discipline name");
            return false;
        }

        if( teacherComboBox.getSelectedIndex() < 0 )
        {
            errorLabel.setText("Pick a teacher");
            return false;
        }

        if( courseComboBox.getSelectedIndex() < 0 )
        {
            errorLabel.setText("Pick a course");
            return false;
        }

        return true;
    }

