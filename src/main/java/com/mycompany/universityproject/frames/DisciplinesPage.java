    private void initEntityTable() {
        String[] columnNames = {"Name", "Teacher", "Speciality", "Course"};

        DisciplineService disciplineService = new DisciplineService();
        List<Discipline> disciplines = disciplineService.getAll();

        Object[][] data = new Object[disciplines.size()][columnNames.length];

        int index = 0;
        for(Discipline discipline : disciplines) {
            data[index][0] = discipline.getName();
            data[index][1] = discipline.getTeacher().getName();
            data[index][2] = discipline.getCourse().getSpeciality().getShortName();
            data[index][3] = discipline.getCourse().getCourseNumber();
            index++;
        }

        DefaultTableModel model = new DefaultTableModel(data, columnNames);

        entityTable.setModel(model);
        entityTable.setGridColor(new Color(255, 255, 255));
        entityTable.setRowHeight(20);
        entityTable.setSelectionBackground(new Color(45, 118, 232));
        scrollPane.setViewportView(entityTable);

        listPanel.add(scrollPane);
    }

