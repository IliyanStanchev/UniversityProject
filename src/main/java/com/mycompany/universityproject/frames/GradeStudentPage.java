    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed

        if( !validateData() )
        return;

        if( studentGrade == null )
        studentGrade = new StudentGrade();

        studentGrade.setStudent(currentStudent);
        studentGrade.setDiscipline(currentDiscipline);
        studentGrade.setGrade((Grade)groupCombo.getSelectedItem());

        studentGradeService.saveOrUpdate(studentGrade);
        changeGradeListener.OnChangeGradeListener();

        dispose();
    }//GEN-LAST:event_saveButtonActionPerformed

    private boolean validateData() {

        errorLabel.setText("");
        if( groupCombo.getSelectedIndex() < 0 )
        {
            errorLabel.setText("Fill student grade");
            return false;
        }

        return true;
    }

