    public void setData(Student currentStudent, Discipline currentDiscipline, ChangeGradeListener changeGradeListener) {

        this.currentStudent = currentStudent;
        this.currentDiscipline = currentDiscipline;
        this.changeGradeListener = changeGradeListener;

        this.studentInformationLabel.setText(currentStudent.getUser().getName());
        this.additionalInformationLabel.setText(currentStudent.getDetails());
        this.studentGrade = studentGradeService.getStudentGrade(currentDiscipline.getId(),currentStudent.getId());

        List<Grade> groupNumbers = Arrays.asList(Grade.values());
        DefaultComboBoxModel defaultComboBoxModel = new DefaultComboBoxModel();
        defaultComboBoxModel.addAll(groupNumbers);
        groupCombo.setModel(defaultComboBoxModel);

        if( studentGrade == null )
            return;

        groupCombo.setSelectedItem(studentGrade.getGrade());
    }

